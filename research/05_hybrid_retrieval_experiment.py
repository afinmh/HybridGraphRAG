def safe_mistral_call(payload, max_retries=3):
    """Fungsi pembantu untuk memanggil API Mistral dengan proteksi Error & Rate Limit."""
    headers = {"Authorization": f"Bearer {MISTRAL_API_KEY}"}
    for attempt in range(max_retries):
        try:
            resp = requests.post(
                "https://api.mistral.ai/v1/chat/completions",
                headers=headers,
                json=payload,
                timeout=30
            )

            if resp.status_code == 200:
                data = resp.json()
                if 'choices' in data and len(data['choices']) > 0:
                    return data['choices'][0]['message']['content']

            elif resp.status_code == 429:
                wait_time = (attempt + 1) * 5
                print(f"⚠️ Rate limit (429). Menunggu {wait_time} detik...")
                time.sleep(wait_time)
            else:
                print(f"❌ API Error {resp.status_code}: {resp.text}")

        except Exception as e:
            print(f"⚠️ Percobaan {attempt+1} gagal: {str(e)}")
            time.sleep(2)

    return None

def get_hybrid_context(query, v_rpc, g_table):
    mq_prompt = f"Write 3 search queries in English to find research papers about: '{query}'. Return ONLY a JSON array of strings."
    queries = [query]
    mq_res = safe_mistral_call({"model": "mistral-small-latest", "messages": [{"role": "user", "content": mq_prompt}]})

    if mq_res:
        try:
            expanded = json.loads(re.sub(r"```json\n?|\n?```", "", mq_res))
            queries.extend(expanded)
        except: pass

    all_raw_docs = []
    all_g_rels = []

    for q in queries[:3]:
        # --- JALUR VECTOR ---
        try:
            q_vec = model_bge.encode([q], normalize_embeddings=True)[0].tolist()
            v_res = supabase_client.rpc(v_rpc, {"query_embedding": q_vec, "match_threshold": 0.05, "match_count": 15}).execute()
            if v_res.data:
                all_raw_docs.extend([d['text_content'] for d in v_res.data])
        except: pass

        # --- JALUR GRAPH ---
        ent_prompt = f"Identify the primary herb and the disease in: '{q}'. Return JSON: ['herb', 'disease']"
        ent_res = safe_mistral_call({"model": "mistral-small-latest", "messages": [{"role": "user", "content": ent_prompt}]})

        entities = []
        if ent_res:
            try:
                entities = json.loads(re.sub(r"```json\n?|\n?```", "", ent_res))
            except: entities = q.split()
        else:
            entities = q.split()

        for ent in entities:
            safe_ent = re.sub(r"[^a-zA-Z0-9\s]", "", str(ent))
            if len(safe_ent) < 3: continue

            res_g = supabase_client.table(g_table).select('*')\
                .or_(f"entity_1.ilike.%{safe_ent}%,entity_2.ilike.%{safe_ent}%")\
                .limit(5).execute()

            for row in res_g.data:
                all_g_rels.append(f"{row['entity_1']} {str(row['relation']).replace('_',' ')} {row['entity_2']}.")

    # 3. Reranking Jalur Vector
    unique_raw_docs = list(set(all_raw_docs))
    v_docs = []
    if unique_raw_docs:
        scores = reranker.predict([[query, doc] for doc in unique_raw_docs])
        v_docs = [doc for _, doc in sorted(zip(scores, unique_raw_docs), reverse=True)[:12]]

    return v_docs, list(set(all_g_rels))[:15]

