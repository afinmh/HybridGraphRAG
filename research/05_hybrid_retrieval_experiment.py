def ask_mistral_hybrid(query, v_docs, g_rels):
    v_txt = "\n".join([f"- {d}" for d in v_docs])
    g_txt = "\n".join([f"- {r}" for r in g_rels])

    user_prompt = f"""
    Context 1 (Scientific Relations):
    {g_txt}

    Context 2 (Research Abstracts):
    {v_txt}

    Question: {query}

    Instructions for High Correctness:
    1. Identify the specific herb mentioned in the context.
    2. Look for any researchers, institutions (e.g., Harvard), or years (e.g., 2019) associated with it.
    3. Describe its biological properties (e.g., anti-inflammatory, antioxidant) ONLY if stated.
    4. Write a cohesive 4-6 sentence paragraph.
    5. Do not hallucinate. If a detail is not in the contexts, do not include it.
    6. Ensure your answer is as detailed as a formal medical abstract.

    Answer (English):"""

    payload = {
        "model": "mistral-small-latest",
        "messages": [
            {"role": "system", "content": "You are a medical research expert. Focus on factual extraction."},
            {"role": "user", "content": user_prompt}
        ],
        "temperature": 0
    }

    response_content = safe_mistral_call(payload)

    if response_content:
        return response_content.strip()
    else:
        return "Error: Unable to generate answer due to API failure or rate limiting."

