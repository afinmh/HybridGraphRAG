import pandas as pd
import numpy as np

def fetch_all_graph_data(table_name):
    """Fungsi untuk mengambil seluruh data dari Supabase melampaui limit 1000."""
    all_data = []
    limit = 1000
    offset = 0

    while True:
        res = supabase_client.table(table_name).select("entity_1, relation, entity_2")\
            .range(offset, offset + limit - 1).execute()

        data = res.data
        if not data:
            break

        all_data.extend(data)
        if len(data) < limit: # Berarti sudah sampai halaman terakhir
            break
        offset += limit

    return all_data

def get_kg_statistics_full():
    tables = {
        "Sentence Split": "graph_sentence",
        "Word Split": "graph_word",
        "Character Split": "graph_character"
    }

    summary_data = []
    print("📊 Memulai Analisis Perbandingan Knowledge Graph (Full Data Sync)...")

    for method_name, table_name in tables.items():
        try:
            # Mengambil data menggunakan fungsi pagination agar > 1000 baris
            data = fetch_all_graph_data(table_name)

            if not data:
                print(f"⚠️ Tabel {table_name} kosong, skip.")
                continue

            df_graph = pd.DataFrame(data)

            # Menghitung Unique Nodes
            all_entities = pd.concat([df_graph['entity_1'], df_graph['entity_2']]).dropna()
            unique_nodes = len(all_entities.unique())

            # Menghitung Unique Edges
            unique_edges = len(df_graph.drop_duplicates(subset=['entity_1', 'relation', 'entity_2']))

            summary_data.append({
                "Method": method_name,
                "Total Triplets": len(df_graph),
                "Unique Nodes": unique_nodes,
                "Unique Edges": unique_edges,
                "Connectivity (Edge/Node)": round(unique_edges / unique_nodes, 2) if unique_nodes > 0 else 0,
                "Most Frequent Entity": all_entities.mode()[0] if not all_entities.empty else "-"
            })

        except Exception as e:
            print(f"❌ Error saat menganalisis {table_name}: {e}")

    # Tampilkan Tabel Final
    if summary_data:
        df_summary = pd.DataFrame(summary_data)
        print("\n" + "="*95)
        print("🏆 PERBANDINGAN STRUKTUR KNOWLEDGE GRAPH - DATA LENGKAP")
        print("="*95)
        print(df_summary.to_string(index=False))
        print("="*95)
    else:
        print("❌ Tidak ada data graf yang ditemukan.")

# Jalankan Analisis Full Data
get_kg_statistics_full()