"""### 2. Teks Cleaning (Regex Lanjutan + AI Pattern)"""

def clean_academic_text(pages_text, metadata):
    cleaned_full_text = ""
    patterns = [metadata.get('header_even'), metadata.get('footer_even'),
                metadata.get('header_odd'), metadata.get('footer_odd')]

    valid_patterns = []
    for p in patterns:
        if p:
            p_str = str(p) if not isinstance(p, dict) else str(list(p.values())[0])
            if len(p_str.strip()) > 3: valid_patterns.append(p_str.strip())

    for p_text in pages_text:
        cleaned_page = p_text
        for pattern in valid_patterns:
            if re.search(r'^(Figure|Fig\.|Table|Tabel)\s*\d+', pattern, re.I): continue
            parts = [part.strip() for part in pattern.split('\n') if len(part.strip()) > 5]
            for part in parts:
                cleaned_page = re.sub(f"^.*?{re.escape(part)}.*?$", "", cleaned_page, flags=re.M | re.I)

        # Standar Regex Cleaning
        cleaned_page = re.sub(r'^(Figure|Fig\.|Table|Tabel|Gambar)\s*\d+[:.].{0,200}$', '', cleaned_page, flags=re.M | re.I)
        cleaned_page = re.sub(r'DOI\s*:?\s*10\.\d{4,9}/[-._;()/:A-Za-z0-9]+', '', cleaned_page, flags=re.I)
        cleaned_page = re.sub(r'^\s*\d+\s*$', '', cleaned_page, flags=re.M)

        cleaned_full_text += cleaned_page.strip() + "\n\n"

    # Cutting: Mulai dari Abstrak, buang Referensi
    start_match = re.search(r'\b(ABSTRACT|ABSTRAK|INTRODUCTION)\b', cleaned_full_text, re.I)
    if start_match: cleaned_full_text = cleaned_full_text[start_match.start():]

    end_match = re.search(r'\n\s*(REFERENCES?|DAFTAR\s+PUSTAKA|BIBLIOGRAPHY)\s*\n', cleaned_full_text, re.I)
    if end_match: cleaned_full_text = cleaned_full_text[:end_match.start()].strip()

    return cleaned_full_text

"""### 3. Splitting/Chunking Engine"""

import re
from transformers import AutoTokenizer

# Inisialisasi Tokenizer
tokenizer_bge = AutoTokenizer.from_pretrained("BAAI/bge-small-en-v1.5")

def get_normalized_text(text):
    """Menghapus newline dan spasi ganda."""
    return re.sub(r'\s+', ' ', text.replace("\n", " ")).strip()

def get_real_token_count(text):
    """Menghitung token BGE tanpa special tokens."""
    return len(tokenizer_bge.encode(text, add_special_tokens=False))

def chunk_by_characters(normalized_text, char_limit=2000, max_tokens=510):
    """
    LOGIKA MURNI CHARACTER: Memotong per jumlah karakter (huruf).
    Jika potongan karakter > 510 token, dipotong tepat di token 510.
    """
    chunks = []
    for i in range(0, len(normalized_text), char_limit):
        chunk_text = normalized_text[i : i + char_limit]

        tokens = tokenizer_bge.encode(chunk_text, add_special_tokens=False)
        if len(tokens) > max_tokens:
            chunk_text = tokenizer_bge.decode(tokens[:max_tokens], skip_special_tokens=True)

        if chunk_text.strip():
            chunks.append(chunk_text)
    return chunks

def chunk_by_words(normalized_text, max_tokens=510):
    """
    LOGIKA MURNI WORD: Memotong per kata (spasi).
    TIDAK AKAN MEMOTONG DI TENGAH KATA.
    Jika kata selanjutnya menyebabkan > 510 token, masuk ke chunk baru.
    """
    words = normalized_text.split()
    chunks = []
    current_chunk_words = []
    current_tokens = 0

    for word in words:
        # Tambahkan spasi untuk simulasi penggabungan kata
        word_with_space = word + " "
        word_tokens = get_real_token_count(word_with_space)

        # Jika kata tunggal saja sudah > 510 token (sangat jarang), potong paksa
        if word_tokens > max_tokens:
            if current_chunk_words:
                chunks.append(" ".join(current_chunk_words))
                current_chunk_words = []
                current_tokens = 0

            # Fallback: potong kata raksasa ini per token
            s_tokens = tokenizer_bge.encode(word, add_special_tokens=False)
            for j in range(0, len(s_tokens), max_tokens):
                chunks.append(tokenizer_bge.decode(s_tokens[j : j + max_tokens], skip_special_tokens=True))
            continue

        if current_tokens + word_tokens <= max_tokens:
            current_chunk_words.append(word)
            current_tokens += word_tokens
        else:
            # Simpan chunk saat ini, mulai chunk baru dengan kata yang sekarang
            chunks.append(" ".join(current_chunk_words))
            current_chunk_words = [word]
            current_tokens = word_tokens

    if current_chunk_words:
        chunks.append(" ".join(current_chunk_words))

    return chunks

def chunk_by_sentences(normalized_text, max_tokens=510):
    """
    LOGIKA MURNI SENTENCE: Berhenti pada tanda titik.
    Menggabungkan kalimat sampai hampir menyentuh 510 token.
    """
    # Gunakan regex untuk menjaga tanda baca
    sentences = [s.strip() for s in re.split(r'(?<=[.!?])\s+', normalized_text) if s.strip()]
    chunks = []
    current_chunk = []
    current_tokens = 0

    for sent in sentences:
        sent_tokens = get_real_token_count(sent + " ")
        if current_tokens + sent_tokens <= max_tokens:
            current_chunk.append(sent)
            current_tokens += sent_tokens
        else:
            if current_chunk:
                chunks.append(" ".join(current_chunk))
            current_chunk = [sent]
            current_tokens = sent_tokens

    if current_chunk:
        chunks.append(" ".join(current_chunk))
    return chunks

