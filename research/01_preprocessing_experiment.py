def patch_specific_anomalies_int(table_name="chunk_sentence2", limit=512):
    print(f"🛠️ Memulai pencarian {table_name} yang > {limit} token...")

    try:
        res = supabase_client.table(table_name).select("*").execute()
        rows = res.data
    except Exception as e:
        print(f"❌ Gagal mengambil data: {e}")
        return

    to_delete = []
    to_insert = []

    for row in rows:
        text = row['text_content']
        tokens = tokenizer.encode(text, add_special_tokens=False)
        token_count = len(tokens)

        if token_count > limit:
            print(f"📍 Mengolah ID {row['id']} (Index: {row['chunk_index']}) | {token_count} tokens...")

            to_delete.append(row['id'])

            # 2. BAGI DUA (Split 50/50)
            mid = token_count // 2
            part1_text = tokenizer.decode(tokens[:mid], skip_special_tokens=True)
            part2_text = tokenizer.decode(tokens[mid:], skip_special_tokens=True)

            parts = [part1_text, part2_text]

            # 3. Siapkan Record Baru dengan Integer Index (Contoh: 23 -> 2301, 2302)
            for idx, p_text in enumerate(parts):
                new_emb = model_embedding.encode(p_text, normalize_embeddings=True).tolist()

                # Kita kalikan 100 lalu tambah urutan (1, 2)
                # Misal index 23 jadi 2301 dan 2302
                new_index = (int(row["chunk_index"]) * 100) + (idx + 1)

                to_insert.append({
                    "journal_id": row["journal_id"],
                    "chunk_index": new_index,
                    "text_content": p_text,
                    "embedding": new_emb,
                    "sentence_count": len(re.split(r'(?<=[.!?])\s+', p_text))
                })

    # 4. Eksekusi ke Supabase
    if to_insert:
        try:
            # Hapus baris lama
            supabase_client.table(table_name).delete().in_("id", to_delete).execute()
            # Masukkan baris baru
            supabase_client.table(table_name).insert(to_insert).execute()
            print(f"\n✅ BERHASIL: {len(to_delete)} anomali dipecah menjadi {len(to_insert)} baris baru (Format Integer).")
        except Exception as e:
            print(f"❌ Gagal update database: {e}")
    else:
        print("✅ Tidak ada anomali yang ditemukan.")

