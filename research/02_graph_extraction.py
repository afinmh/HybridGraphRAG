def process_and_save_graphs_v2(chunk_table_name: str, graph_table_name: str, limit_per_journal=5):
    """
    Ekstraksi Knowledge Graph khusus untuk tabel V2 (Experiment 2).
    """
    # 1. Ambil list jurnal yang sudah diproses di V2
    res_journals = supabase_client.table('journals_experiment').select('id, title').execute()
    journals = res_journals.data

    if not journals:
        print("❌ Tidak ada jurnal di Database!")
        return

    print(f"\n🚀 MEMULAI EKSTRAKSI GRAF: {chunk_table_name} -> {graph_table_name}")
    print(f"⚙️ Limit: {limit_per_journal} chunks per jurnal (Gratisan Mistral Mode)")

    for journal in journals:
        j_id = journal['id']
        j_title = journal['title']

        # Opsi: Bersihkan data graf lama untuk jurnal ini di tabel target jika ingin refresh
        # supabase_client.table(graph_table_name).delete().eq('journal_id', j_id).execute()

        # 2. Ambil chunk dari tabel V2 terkait
        res_chunks = supabase_client.table(chunk_table_name).select('id, text_content, chunk_index')\
            .eq('journal_id', j_id)\
            .order('chunk_index')\
            .limit(limit_per_journal).execute()

        chunks = res_chunks.data
        if not chunks:
            continue

        print(f"\n>>> Jurnal: {j_title[:50]}...")
        total_inserted = 0

        for chunk in chunks:
            idx = chunk['chunk_index']
            print(f"   - Processing Chunk Index: {idx} (ID: {chunk['id'][:8]})")

            raw_text = chunk['text_content']
            if len(raw_text) < 100: # Filter teks sampah
                continue

            try:
                # --- Tier 1: Medical Summary ---
                # (Pastikan fungsi summarize_medical_text sudah didefinisikan sebelumnya)
                summary = summarize_medical_text(raw_text)
                if not summary: continue

                time.sleep(3) # Jeda untuk Rate Limit Mistral

                # --- Tier 2: Ontology Extraction ---
                # (Pastikan fungsi extract_biological_graph sudah didefinisikan sebelumnya)
                graph_data = extract_biological_graph(summary)

                # --- Tier 3: Insert to V2 Graph Table ---
                inserted_in_chunk = 0
                for rel in graph_data:
                    # Validasi struktur Array ["Entity1", "Type1", "Relation", "Entity2", "Type2"]
                    if isinstance(rel, list) and len(rel) == 5:
                        supabase_client.table(graph_table_name).insert({
                            "journal_id": j_id,
                            "chunk_id": chunk['id'],
                            "entity_1": rel[0],
                            "type_1": rel[1],
                            "relation": rel[2],
                            "entity_2": rel[3],
                            "type_2": rel[4]
                        }).execute()
                        inserted_in_chunk += 1

                print(f"      ✅ Berhasil: {inserted_in_chunk} relasi ditemukan.")
                total_inserted += inserted_in_chunk

                # Delay antar chunk agar API stabil
                time.sleep(5)

            except Exception as e:
                print(f"      ⚠️ Error pada chunk {idx}: {e}")
                time.sleep(10) # Istirahat lebih lama jika error

        print(f"🏁 Selesai: {j_title[:30]}. Total Graf: {total_inserted}")

